import fs from 'node:fs/promises';
import path from 'node:path';

const rootDir = process.cwd();
const outputPath = path.join(rootDir, 'lib', 'urongda-school-logo-map.ts');
const logosUrl = 'https://www.urongda.com/logos';

const requestHeaders = {
  'user-agent': 'Mozilla/5.0 (compatible; SeekofferLogoSync/1.0; +https://www.seekoffer.com.cn)',
  accept: 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8'
};

function normalizeSchoolName(value) {
  return String(value || '')
    .normalize('NFKC')
    .replace(/\s+/g, '')
    .replace(/[()（）]/g, '')
    .trim();
}

function extractLogoEntries(html) {
  const pattern =
    /<img[^>]+src=["'](https:\/\/cdn\.urongda\.com\/images\/schools\/[^"']+)["'][^>]+alt=["']([^"']*?)\u6821\u5fbd/giu;
  const entries = new Map();

  for (const [, imageUrl, rawName] of html.matchAll(pattern)) {
    const name = normalizeSchoolName(rawName.replace(/\u77e2\u91cf\u56fe$/, ''));
    if (!name || entries.has(name)) {
      continue;
    }

    entries.set(name, imageUrl);
  }

  return [...entries.entries()].sort(([left], [right]) => left.localeCompare(right, 'zh-CN'));
}

async function main() {
  const response = await fetch(logosUrl, {
    headers: requestHeaders,
    redirect: 'follow'
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${logosUrl}: ${response.status} ${response.statusText}`);
  }

  const html = await response.text();
  const entries = extractLogoEntries(html);

  if (entries.length < 100) {
    throw new Error(`Only parsed ${entries.length} logo entries from ${logosUrl}. Refusing to overwrite map.`);
  }

  const body = entries
    .map(([name, imageUrl]) => `  ${JSON.stringify(name)}: ${JSON.stringify(imageUrl)}`)
    .join(',\n');

  const source = `// Generated by scripts/sync-urongda-logo-map.mjs.\n// Source: ${logosUrl}\n\nexport const urongdaSchoolLogoMap: Record<string, string> = {\n${body}\n};\n`;

  await fs.writeFile(outputPath, source, 'utf8');
  console.log(`Wrote ${entries.length} school logos to ${path.relative(rootDir, outputPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
